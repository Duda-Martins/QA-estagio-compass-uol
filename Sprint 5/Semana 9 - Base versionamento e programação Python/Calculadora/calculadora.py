class Calculadora:
    # Define atributos e m√©todos aqui
    def __init__(self):
        pass

    def soma(self, n1, n2):
        resultado = n1 + n2
        return resultado

    def subtracao(self, n1, n2):
        resultado = n1 - n2
        return resultado
    
    def multiplicacao(self, n1, n2):
        resultado = n1 * n2
        return resultado
    
    def divisao(self, n1, n2):
        resultado = n1 / n2
        return resultado
    
    def potenciacao(self, n1, n2):
        resultado = n1 ** n2
        return resultado
    
    def media(self, n1, n2):
        resultado = (n1 + n2) / 2
        return resultado
